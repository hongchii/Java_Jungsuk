package CafeOrderProgram;

public class MenuVO {
	private String coffee;
	private String iceOrHot;
	private String shot;
	private String size;
	private String takeoutYn;
	
	public String getCoffee() {
		return coffee;
	}
	public void setCoffee(String coffee) {
		this.coffee = coffee;
	}
	public String getIceOrHot() {
		return iceOrHot;
	}
	public void setIceOrHot(String iceOrHot) {
		this.iceOrHot = iceOrHot;
	}
	public String getShot() {
		return shot;
	}
	public void setShot(String shot) {
		this.shot = shot;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getTakeoutYn() {
		return takeoutYn;
	}
	public void setTakeoutYn(String takeoutYn) {
		this.takeoutYn = takeoutYn;
	}
	
	
}
