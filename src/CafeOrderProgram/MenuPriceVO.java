package CafeOrderProgram;

public class MenuPriceVO {
	private int coffeePrice;
	private int iceOrHotPrice;
	private int shotPrice;
	private int sizePrice;
	private int takeoutYnPrice;
	
	public int getCoffeePrice() {
		return coffeePrice;
	}
	public void setCoffeePrice(int coffeePrice) {
		this.coffeePrice = coffeePrice;
	}
	public int getIceOrHotPrice() {
		return iceOrHotPrice;
	}
	public void setIceOrHotPrice(int iceOrHotPrice) {
		this.iceOrHotPrice = iceOrHotPrice;
	}
	public int getShotPrice() {
		return shotPrice;
	}
	public void setShotPrice(int shotPrice) {
		this.shotPrice = shotPrice;
	}
	public int getSizePrice() {
		return sizePrice;
	}
	public void setSizePrice(int sizePrice) {
		this.sizePrice = sizePrice;
	}
	public int getTakeoutYnPrice() {
		return takeoutYnPrice;
	}
	public void setTakeoutYnPrice(int takeoutYnPrice) {
		this.takeoutYnPrice = takeoutYnPrice;
	}
	
	
}
